#include <minishell.h>

void	ft_echo(char *cmd)
{
	int		i;

	i = 4;
	while (cmd[i] && (cmd[i] == ' ' || cmd[i] == '\t'))
		i++;
	while (cmd[i])
	{
		if (cmd[i] != '"' && cmd[i] != 39 && cmd[i] != 47)
			write(1, &(cmd[i]), 1);
		i++;
	}
	write(1, "\n", 1);
}
