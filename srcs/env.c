#include <minishell.h>

void	ft_env(char *cmd, char **av)
{
	int		i;

	i = 2;
	(void)cmd;
	while (av[i])
	{
		ft_putstr(av[i]);
		write(1, "\n", 1);
		i++;
	}
}
